#include "includes/libft.h"

void		ft_strrev(char *s)
{
	int		i;
	int 	j;
	char	c;

	i = ft_strlen(s) - 1;
	j = 0;
	while (j < i)
	{
		c = s[j];
		s[j] = s[i];
		s[i] = c;
		i--;
		j++;
	}
}
